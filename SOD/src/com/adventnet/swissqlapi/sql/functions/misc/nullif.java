   public void toOracle(SelectQueryStatement to_sqs, SelectQueryStatement from_sqs) throws ConvertException {
      Vector arguments = new Vector();
      boolean isdenodo = from_sqs != null && from_sqs.isDenodo();

      for(int i_count = 0; i_count < this.functionArguments.size(); ++i_count) {
         if (this.functionArguments.elementAt(i_count) instanceof SelectColumn) {
            arguments.addElement(((SelectColumn)this.functionArguments.elementAt(i_count)).toOracleSelect(to_sqs, from_sqs));
         } else {
            arguments.addElement(this.functionArguments.elementAt(i_count));
         }
      }

      this.setFunctionArguments(arguments);
      if (isdenodo) {
         this.functionName.setColumnName("NULLIF");
      } else if (this.functionArguments.size() == 2) {
         this.functionName.setColumnName("DECODE");
         Object param1 = this.functionArguments.get(0);
         Object param2 = this.functionArguments.get(1);
         this.functionArguments.setSize(1);
         StringBuffer argBuffer = new StringBuffer();
         argBuffer.append(param1.toString());
         argBuffer.append(" , ");
         argBuffer.append(param2.toString());
         argBuffer.append(" , NULL , ");
         argBuffer.append(param1.toString());
         Vector arg = new Vector();
         arg.add(argBuffer.toString());
         this.setFunctionArguments(arg);
      }

   }

